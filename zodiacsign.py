dob = input("Введите дату рождения в формате ДД-ММ-ГГГГ: ")
dob_date = datetime.datetime.strptime(dob, "%d-%m-%Y")
zodiac_sign = None
for sign, dates in zodiac_dates.items():
    start_date = datetime.datetime.strptime(dates[0] + "-" + str(dob_date.year), "%m-%d-%Y")
    end_date = datetime.datetime.strptime(dates[1] + "-" + str(dob_date.year), "%m-%d-%Y")
    if start_date <= dob_date <= end_date:
        zodiac_sign = sign
        break
